import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getBags = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/bags.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const deleteBag = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/bags/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const createBag = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/bags.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updateBag = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/bags/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

const getSingleBag = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/bags.json?orderBy="firebaseKey"&equalTo="${firebaseKey}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

export {
  getBags, deleteBag, createBag, updateBag, getSingleBag,
};
